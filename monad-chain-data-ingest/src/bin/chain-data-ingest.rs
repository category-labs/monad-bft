use clap::Parser;
use eyre::Result;
use monad_chain_data_ingest::Cli;
use tracing::Level;

#[tokio::main]
async fn main() -> Result<()> {
    tracing_subscriber::fmt()
        .with_max_level(Level::INFO)
        .with_env_filter(
            tracing_subscriber::EnvFilter::builder()
                .with_default_directive(Level::INFO.into())
                .from_env_lossy(),
        )
        .init();

    monad_chain_data_ingest::run(Cli::parse()).await
}
